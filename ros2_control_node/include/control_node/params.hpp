// ============================================================================
//  params.hpp  —  ★튜닝 파라미터 전부 모음 (튜닝할 때 이 파일만 보면 됨)
//
//  세 그룹:
//   - ControllerParams : 모드 제어기 게인/한계 (FollowController 등이 공유)
//   - ObstacleParams   : 장애물 회피(ObstacleField) 거리 임계값
//   - NodeParams       : 노드 운영값 (제어 주기, 타임아웃, 시작 모드)
//
//  ※ 여기 적힌 값은 "코드 기본값". 실행 시엔 config/control_params.yaml 이
//    덮어쓴다. 두 파일의 기본값을 항상 일치시켜 둘 것.
//  ※ ROS 파라미터 이름 ↔ 구조체 필드 매핑은 ControlNode::declareParams()/
//    loadParams() (src/control_node.cpp) 한 곳에서만 한다.
// ============================================================================
#ifndef CONTROL_NODE__PARAMS_HPP_
#define CONTROL_NODE__PARAMS_HPP_

namespace control_node
{

// ----------------------------------------------------------------------------
//  ControllerParams : 모드 제어기 게인/한계.
//  ControlNode 가 ROS 파라미터에서 읽어 채워 넘긴다(제어기는 ROS 무관).
// ----------------------------------------------------------------------------
struct ControllerParams
{
  // ========================================================================
  //  ★안전 한계 — 실주행 때 제일 먼저/자주 만질 값 (이 블록만 보면 됨)
  //
  //  속도·가속 상한은 발행 직전 최종 클램프(applySafetyLimits, control_node)
  //  로 한 번 더 강제된다 → 어떤 모드/제어기 버그가 있어도 절대 못 넘는다.
  // ========================================================================
  double v_max          = 0.4;   // m/s, 몸체 평면속도 상한 (벡터 크기 기준)
                                 //  ★휠모터(JGB37-520) 토크 여유 적음 → 0.4 유지.
                                 //   올리려면 실차 전류/발열 확인 후 조금씩.
  double w_body_max     = 0.7;   // rad/s, 몸체 각속도 상한
                                 //  ※회전도 휠 토크를 씀. 직진+회전 동시 시
                                 //   휠 부하가 더 커지니 토크 빠듯하면 같이 낮출 것.
  double body_accel_max = 0.5;   // m/s^2, 평면 가속 상한 (슬루) — 미끄럼/급발진 방지
  double yaw_accel_max  = 1.5;   // rad/s^2, 각가속 상한 (슬루)
  double w_top_max      = 1.5;   // rad/s, 상단yaw 목표각 변화율 상한 (스텝 보호)
  double top_lead_max   = 0.8;   // rad, 상단yaw 목표가 실측각(theta_head)보다
                                 //  앞설 수 있는 최대 리드. 드라이버가 멈추거나
                                 //  늦어도 목표각 폭주를 차단. 0 이하=비활성

  // ------------------------------------------------------------------------
  //  상단 yaw 게인 (카메라를 주인에 락온; NEMA17+A4988 스텝 → 위치 명령)
  // ------------------------------------------------------------------------
  double kp_yaw  = 0.7;     // azimuth → 상단yaw 목표각 보정 게인 (0~1)
  double az_dead = 0.03;    // rad, azimuth 불감대 (떨림 방지)

  // ------------------------------------------------------------------------
  //  몸체 위치 게인 (선분 끝점 추종; 메카넘 → 속도 명령, PD)
  // ------------------------------------------------------------------------
  double kp_pos   = 0.7;    // 위치오차[m] → 속도[m/s] (P 게인)
  double kd_pos   = 0.15;   // 오차 변화율 감쇠 (D 게인). 0이면 P만.
                            //  ★실주행 튜닝 순서: kd=0 → kp → 출렁이면 kd.
                            //   추정 노이즈가 크면 D가 떨림을 만들 수 있음.
  double pos_dead = 0.08;   // m, 위치 불감대 (도착 판정)

  // ------------------------------------------------------------------------
  //  몸체 yaw 게인 (글로벌 헤딩 유지, PD; 모드1·2 공용)
  // ------------------------------------------------------------------------
  double kp_byaw   = 1.0;   // yaw오차[rad] → 각속도[rad/s] (P 게인)
  double kd_byaw   = 0.1;   // yaw 감쇠 (D 게인). 0이면 P만.
  double byaw_dead = 0.05;  // rad, yaw 불감대

  // ------------------------------------------------------------------------
  //  선분(목표) 기본값 — 진입 시 캡처가 원칙. 추정 실패 시 폴백값
  // ------------------------------------------------------------------------
  double seg_distance = 1.5;   // m, 유지할 거리 D 기본값
  double seg_d_min    = 0.5;   // m, 손동작 조정으로 줄일 수 있는 최소 거리
  double seg_d_max    = 3.0;   // m, 손동작 조정으로 늘릴 수 있는 최대 거리
  //  ※ 클램프는 손동작(/adjust_cmd) 경로에만 적용. engage 캡처는 실거리
  //    그대로 사용 — 진입 순간 로봇이 움직이지 않는다는 약속을 지키기 위함.
  // 글로벌 각도(seg_angle)는 모드 진입 순간 캡처하므로 파라미터 아님.

  // ------------------------------------------------------------------------
  //  몸체 헤딩 정책
  //   true  = 로봇 정면이 항상 주인을 향하도록 yaw 목표를 매 프레임 계산
  //   false = 진입 시 선분각(φ) 방향 헤딩 유지
  // ------------------------------------------------------------------------
  bool face_owner = true;

  // ------------------------------------------------------------------------
  //  리프트 (NEMA23+DM542 스텝 → 위치 명령)
  // ------------------------------------------------------------------------
  double z_min        = 0.0;   // m, 행정 하한
  double z_max        = 0.5;   // m, 행정 상한
  double lift_default = 0.0;   // m, 모드1 기본 높이

  // --- 상단 yaw 언와인딩 (TODO: 지금은 자리만, 사실상 무한) ---
  double theta_soft_max = 1.0e9;   // rad (나중에 ~±1.8바퀴로 축소 예정)
};

// ----------------------------------------------------------------------------
//  ObstacleParams : 장애물 회피 거리 임계값 (ObstacleField)
// ----------------------------------------------------------------------------
struct ObstacleParams
{
  bool   enabled   = true;   // false면 회피 보정 자체를 끔
  double stop_dist = 0.20;   // m, 이 거리 이하면 그 방향 이동 완전 차단
  double slow_dist = 0.60;   // m, 이 거리부터 감속 시작 (> stop_dist)
};

// ----------------------------------------------------------------------------
//  NodeParams : 노드 운영값 (제어 주기 / 입력 타임아웃 / 시작 모드)
// ----------------------------------------------------------------------------
struct NodeParams
{
  int    start_mode        = 0;     // 시작 모드 (0=IDLE). supervisor가 바꿈
  double ctrl_rate         = 50.0;  // Hz, 제어 루프 주기
  double camera_latency    = 0.0;   // s, 카메라(트래킹) 파이프라인 지연 보상.
                                    //  회전 중 azimuth 가 "조금 전" 값이라
                                    //  주인 추정이 회전 방향으로 끌리는 것을
                                    //  azimuth += latency×(몸체+상단 yaw 속도)
                                    //  로 보정. ★기본 0=꺼짐. 실차에서 회전 중
                                    //  추정이 끌리는 게 보이면 0.05~0.15 시도.
                                    //  부호가 반대로 가면 즉시 0으로 되돌릴 것.
  double owner_timeout     = 0.5;   // s, /owner_pose 끊김 판정
  double odom_timeout      = 0.5;   // s, /odom 끊김 판정
  double proximity_timeout = 0.3;   // s, /proximity 끊김 판정(회피 비활성)
  double teleop_timeout    = 0.5;   // s, /teleop_cmd 끊김 판정(모드0 → 정지)
};

}  // namespace control_node

#endif  // CONTROL_NODE__PARAMS_HPP_
