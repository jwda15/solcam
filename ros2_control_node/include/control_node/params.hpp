// ============================================================================
//  params.hpp  —  ★튜닝 파라미터 전부 모음 (튜닝할 때 이 파일만 보면 됨)
//
//  세 그룹:
//   - ControllerParams : 모드 제어기 게인/한계 (FollowController 등이 공유)
//   - ObstacleParams   : 장애물 회피(ObstacleField) 거리 임계값
//   - NodeParams       : 노드 운영값 (제어 주기, 타임아웃, 시작 모드)
//
//  ※ 여기 적힌 값은 "코드 기본값". 실행 시엔 config/control_params.yaml 이
//    덮어쓴다. 두 파일의 기본값을 항상 일치시켜 둘 것.
//  ※ ROS 파라미터 이름 ↔ 구조체 필드 매핑은 ControlNode::declareParams()/
//    loadParams() (src/control_node.cpp) 한 곳에서만 한다.
// ============================================================================
#ifndef CONTROL_NODE__PARAMS_HPP_
#define CONTROL_NODE__PARAMS_HPP_

namespace control_node
{

// ----------------------------------------------------------------------------
//  ControllerParams : 모드 제어기 게인/한계.
//  ControlNode 가 ROS 파라미터에서 읽어 채워 넘긴다(제어기는 ROS 무관).
// ----------------------------------------------------------------------------
struct ControllerParams
{
  // ========================================================================
  //  ★안전 한계 — 실주행 때 제일 먼저/자주 만질 값 (이 블록만 보면 됨)
  //
  //  속도·가속 상한은 발행 직전 최종 클램프(applySafetyLimits, control_node)
  //  로 한 번 더 강제된다 → 어떤 모드/제어기 버그가 있어도 절대 못 넘는다.
  // ========================================================================
  double v_max          = 0.12;  // m/s, 몸체 평면속도 상한 (벡터 크기 기준) [0623 더 느리게]
                                 //  ★실차 모터가 빨라 더 낮춤. control_params.yaml 에서
                                 //   런타임 조정(이제 solcam.sh 가 yaml 을 로드함).
  double w_body_max     = 0.3;   // rad/s, 몸체 각속도 상한 [0623 →0.3: 근거리 과회전 캡]
                                 //  ※회전도 휠 토크를 씀. 직진+회전 동시 시
                                 //   휠 부하가 더 커지니 토크 빠듯하면 같이 낮출 것.
  double body_accel_max = 0.3;   // m/s^2, 평면 가속 상한 (슬루) [0623 0.5→0.3: 더 부드럽게 출발]
  double yaw_accel_max  = 0.8;   // rad/s^2, 각가속 상한 (슬루) [0623 1.5→0.8: 회전 천천히]
  double w_top_max      = 1.5;   // rad/s, 상단yaw 목표각 변화율 상한 (스텝 보호)
  double top_lead_max   = 0.8;   // rad, 상단yaw 목표가 실측각(theta_head)보다
                                 //  앞설 수 있는 최대 리드. 드라이버가 멈추거나
                                 //  늦어도 목표각 폭주를 차단. 0 이하=비활성

  // ------------------------------------------------------------------------
  //  상단 yaw 게인 (카메라를 주인에 락온; NEMA17+A4988 스텝 → 위치 명령)
  // ------------------------------------------------------------------------
  double kp_yaw  = 0.7;     // azimuth → 상단yaw 목표각 보정 게인 (position모드 전용)
  double az_dead = 0.15;    // rad, azimuth 불감대 (이 안이면 상단yaw 정지)
                            //  ★펄스 제어용으로 더 넓힘(0.10→0.15≈±8.6°). 1펄스(~13°)와
                            //   비슷해 헌팅 방지. 더 둔감: 키우기 / 더 민감: 줄이기.
  // ★상단 yaw 펌웨어 제어 방식:
  //   true  = velocity(방향) 모드 — top_yaw_target 의 부호=회전방향, |값|이 펌웨어
  //           deadband 초과면 고정속도 회전(현 펌웨어). 절대각 추적 아님.
  //   false = position(절대각) 모드 (구 펌웨어).
  bool   yaw_velocity_mode = true;
  double top_yaw_sign      = 1.0;   // 회전 방향이 실제와 반대면 -1.0 (배선 보정)

  // ★상단 yaw 안전 회전한계(데드레코닝): OAK-D 케이블 길이 제한 때문에 ±이 각을
  //   넘게 돌면 정지시킨다. 우리가 각도가 아니라 "명령시간"으로 제어하므로 현재각을
  //   순수 데드레코닝(명령부호×속도×dt 적분, 0=부팅/0점지정시점)으로 추정해 판정.
  //   펌웨어도 ±800step(=±90°)에서 하드정지하므로 이 값은 그 이하로 두는 게 안전.
  double top_yaw_limit = 1.5708;     // (구·미사용) 각도 가드 — 시간기반으로 대체됨.
  double top_yaw_limit_pos = 0.0995; // (구·미사용)
  double top_yaw_limit_neg = 0.0698; // (구·미사용)

  // ★상단 yaw = 펄스(잠깐 명령) 제어. 모터가 빠르고(측정 90°/0.14s≈643°/s) 정밀도·배선
  //   접촉이 나빠, 연속추종 대신 "주기마다 짧게 한 번" 톡톡 돌린다.
  bool   yaw_pulse_mode   = true;    // true=펄스(권장) / false=연속(구 동작)
  double yaw_pulse_period = 0.5;     // s, 펄스 간격(이 주기마다 최대 1회)
  double yaw_pulse_sec    = 0.06;    // s, 1회 펄스 길이 [0623 0.02→0.06: 0.02는 거의 안 돎]
  // ★OAK 케이블 꼬임 방어(데드레코닝, 시간기반): 시작/0점지정 후 한 방향으로 "준 명령
  //   시간"을 누적 → ±yaw_time_limit 초 넘으면 그 방향 추가명령 차단(정지). 각도 아님.
  //   0.16s≈103°(<120° 꼬임한계), 0.14s 근처서 UI 하단 빨간선 2s 경고.
  double yaw_time_limit = 1.8;       // s, 한 방향 누적 명령시간 하드 한계 [0625 0.18→1.8(10배): 실측 180°까지 가능]
                                     //  ★180° 넘어 꺾이는 건 시연 때 수동 조정. 더 넓히려면 키우기.
  double yaw_time_warn  = 1.6;       // s, 이 근처 도달 시 /yaw_limit_warn → UI 빨간선
  double top_yaw_speed  = 1.75;      // rad/s, 스테이지 회전속도 [0625 11.22→1.75: 1.8s≈180° 실측 일관].
                                     //  누적시간→각 환산(폴백용). Front/Back/L/R 확정값이 우선.

  // ★거리 의존 펄스: 주인이 가까우면 azimuth가 예민(작은 이동=큰 각)해 펄스가 자주
  //   쌓여 한계에 금방 도달 → 가까울수록 "주기 길게 + 데드존 크게"로 둔감화.
  //   near_dist(이하)~far_dist(이상) 사이를 선형보간(밖은 클램프).
  bool   yaw_depth_scale  = true;    // false면 고정값(yaw_pulse_period, az_dead) 사용
  double yaw_near_dist    = 1.0;     // m, 이 거리 이하 = "가까움"
  double yaw_far_dist     = 3.0;     // m, 이 거리 이상 = "멈"
  double yaw_period_near  = 2.2;     // s, 가까울 때 펄스 주기 [0623 3.0→2.2: 근거리 더 과감]
  double yaw_period_far   = 1.2;     // s, 멀 때 펄스 주기 [0623 1.0→1.2 조금 늦춤]
  double az_dead_near     = 0.28;    // rad, 가까울 때 데드존 [0623 0.35→0.28(≈16°): 근거리 더 과감]
  double az_dead_far      = 0.18;    // rad, 멀 때 데드존(≈10.3°, 작게)

  // ------------------------------------------------------------------------
  //  몸체 위치 게인 (선분 끝점 추종; 메카넘 → 속도 명령, PD)
  // ------------------------------------------------------------------------
  double kp_pos   = 0.4;    // 위치오차[m] → 속도[m/s] (P 게인) [0623 →0.4: 더 부드럽게]
  double kd_pos   = 0.30;   // 오차 변화율 감쇠 (D 게인) [0623 0.15→0.30: 위치 오버슈트 감쇠]
                            //  ★실주행 튜닝 순서: kd=0 → kp → 출렁이면 kd.
                            //   추정 노이즈가 크면 D가 떨림을 만들 수 있음.
  double pos_dead = 0.30;   // m, 위치 불감대 (도착 판정). ★주인 잔움직임 무시 0.15→0.30

  // ------------------------------------------------------------------------
  //  몸체 yaw 게인 (글로벌 헤딩 유지, PD; 모드1·2 공용)
  // ------------------------------------------------------------------------
  double kp_byaw   = 0.30;  // yaw오차[rad] → 각속도[rad/s] (P) [0623 →0.30: 과회전 더↓]
  double kd_byaw   = 0.30;  // yaw 감쇠 (D) [0623 →0.30: 오버슈트 감쇠 강화]
  double byaw_dead = 0.15;  // rad, yaw 불감대 [0623 →0.15(±8.6°): 작은 방위변화 무시→잔회전↓]

  // ------------------------------------------------------------------------
  //  선분(목표) 기본값 — 진입 시 캡처가 원칙. 추정 실패 시 폴백값
  // ------------------------------------------------------------------------
  double seg_distance = 1.5;   // m, 유지할 거리 D 기본값
  double seg_d_min    = 0.5;   // m, 손동작 조정으로 줄일 수 있는 최소 거리
  double seg_d_max    = 3.0;   // m, 손동작 조정으로 늘릴 수 있는 최대 거리
  //  ※ 클램프는 손동작(/adjust_cmd) 경로에만 적용. engage 캡처는 실거리
  //    그대로 사용 — 진입 순간 로봇이 움직이지 않는다는 약속을 지키기 위함.
  // 글로벌 각도(seg_angle)는 모드 진입 순간 캡처하므로 파라미터 아님.

  // ------------------------------------------------------------------------
  //  몸체 헤딩 정책
  //   true  = 로봇 정면이 항상 주인을 향하도록 yaw 목표를 매 프레임 계산
  //   false = 진입 시 선분각(φ) 방향 헤딩 유지
  // ------------------------------------------------------------------------
  bool face_owner = true;

  // ------------------------------------------------------------------------
  //  모드3 FOLLOW2 (leash): 거리만 유지, 구도/odom 불필요. ★데드존 널널.
  // ------------------------------------------------------------------------
  double leash_distance = 1.5;   // m, 유지할 목표 거리
  double leash_dead     = 0.7;   // m, 슬랙(거리+이 값 "넘을 때만" 끌려감) [0623 0.5→0.7 더 둔감]
  double kp_leash       = 0.6;   // 거리오차[m] → 속도[m/s] (P, 부드럽게)

  // ------------------------------------------------------------------------
  //  모드4 ORBIT (공전): 주인 중심 반지름 유지하며 천천히 돌기. odom 불필요.
  // ------------------------------------------------------------------------
  double orbit_speed  = 0.10;   // m/s, 접선(공전) 속도 — ★천천히 [0623 0.15→0.10]
  bool   orbit_ccw    = true;   // true=반시계(CCW) 기본
  double kp_orbit_r   = 0.5;    // 반지름 오차[m] → 반경속도[m/s] (P)

  // ------------------------------------------------------------------------
  //  리프트 (NEMA23+DM542 스텝 → 위치 명령)
  // ------------------------------------------------------------------------
  double z_min        = 0.0;   // m, 행정 하한 (내림 명령 목표)
  double z_max        = 0.5;   // m, 행정 상한 (올림 명령 목표)
  double lift_default = 0.0;   // m, (미사용 — 시간기반 제어로 전환)
  bool   lift_invert  = true;  // ★올림/내림이 실제와 반대면 true (실차 확인=반대라 true)

  // --- 상단 yaw 언와인딩 (TODO: 지금은 자리만, 사실상 무한) ---
  double theta_soft_max = 1.0e9;   // rad (나중에 ~±1.8바퀴로 축소 예정)

  // ----- 거리 의존 보간 헬퍼 (near_dist 이하=vn, far_dist 이상=vf, 사이 선형) -----
  double lerpByDist(double d, double vn, double vf) const
  {
    if (yaw_far_dist <= yaw_near_dist) { return vn; }
    double t = (d - yaw_near_dist) / (yaw_far_dist - yaw_near_dist);
    t = (t < 0.0) ? 0.0 : (t > 1.0 ? 1.0 : t);
    return vn + t * (vf - vn);
  }
  // 주인 거리[m] → 펄스 주기[s] (가까울수록 김=느림). depth_scale off면 고정값.
  double yawPeriodFor(double dist) const
  {
    return yaw_depth_scale ? lerpByDist(dist, yaw_period_near, yaw_period_far)
                           : yaw_pulse_period;
  }
  // 주인 거리[m] → azimuth 데드존[rad] (가까울수록 큼). depth_scale off면 az_dead.
  double azDeadFor(double dist) const
  {
    return yaw_depth_scale ? lerpByDist(dist, az_dead_near, az_dead_far) : az_dead;
  }
};

// ----------------------------------------------------------------------------
//  ObstacleParams : 장애물 회피 거리 임계값 (ObstacleField)
// ----------------------------------------------------------------------------
struct ObstacleParams
{
  bool   enabled   = true;   // false면 회피 보정 자체를 끔
  double stop_dist = 0.20;   // m, 이 거리 이하면 그 방향 이동 완전 차단
  double slow_dist = 0.60;   // m, 이 거리부터 감속 시작 (> stop_dist)
};

// ----------------------------------------------------------------------------
//  NodeParams : 노드 운영값 (제어 주기 / 입력 타임아웃 / 시작 모드)
// ----------------------------------------------------------------------------
struct NodeParams
{
  int    start_mode        = 0;     // 시작 모드 (0=IDLE). supervisor가 바꿈
  bool   freeze_owner      = false; // 기본 false = 실시간 주인 추종(졸졸 따라감, 기존 FOLLOW).
                                    //  true 면 모드확정 순간 주인 위치를 고정 타겟으로
                                    //  잡고 안 따라감(정지 구도용). 보통 false 권장.
  double ctrl_rate         = 50.0;  // Hz, 제어 루프 주기
  double camera_latency    = 0.0;   // s, 카메라(트래킹) 파이프라인 지연 보상.
                                    //  회전 중 azimuth 가 "조금 전" 값이라
                                    //  주인 추정이 회전 방향으로 끌리는 것을
                                    //  azimuth += latency×(몸체+상단 yaw 속도)
                                    //  로 보정. ★기본 0=꺼짐. 실차에서 회전 중
                                    //  추정이 끌리는 게 보이면 0.05~0.15 시도.
                                    //  부호가 반대로 가면 즉시 0으로 되돌릴 것.
  double lift_cmd_timeout  = 0.3;   // s, 리프트 손동작 명령이 이 시간 안 들어오면
                                    //  "손 뗌"으로 보고 리프트 정지(시간기반 제어).
  double wheel_cmd_timeout = 0.4;   // s, 메뉴 열림 중 휠 명령(거리/공전/팬)이 이 시간
                                    //  안 들어오면 몸체 정지(hold). 줄 때만 움직임.
  double yaw_zero_cooldown = 3.0;   // s, rock_on 으로 OAK 0점 지정 직후 상단yaw 정지
                                    //  유지 시간(케이블 안전하게 풀린 상태 정리용)
  double owner_timeout     = 0.5;   // s, /owner_pose 끊김 판정
  double odom_timeout      = 0.5;   // s, /odom 끊김 판정
  double proximity_timeout = 0.3;   // s, /proximity 끊김 판정(회피 비활성)
  double teleop_timeout    = 0.5;   // s, /teleop_cmd 끊김 판정(모드0 → 정지)
};

}  // namespace control_node

#endif  // CONTROL_NODE__PARAMS_HPP_
